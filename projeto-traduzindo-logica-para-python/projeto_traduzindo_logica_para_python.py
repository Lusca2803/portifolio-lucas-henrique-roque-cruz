# Esta função calcula o total de uma compra, aplicando descontos progressivos e validando o estoque.

def processar_vendas():

    total_compra = 0.0
    itens_comprados = 0

    print("Quantos produtos diferentes foram comprados?")
    quantidade_tipos = int(input("Quantidade de tipos: "))

    for i in range(quantidade_tipos):
        print("Nome do produto: ")
        nome = input("O nome é: ")
        print("Preço unitário: ")
        preco = float(input("O preço é: "))
        print("Quantidade deste produto:")
        qtd = int(input("A quantidade é: "))

        if preco <= 0 or qtd <= 0:
            print(f"Erro: Valores inválidos para {nome}. Este item não será adicionado ao total.")
        else:
            subtotal = preco * qtd
            total_compra += subtotal
            itens_comprados += qtd

    if total_compra > 500.00:
        total_final = total_compra * 0.90
        print("Desconto de 10% aplicado!")
    elif total_compra > 200.00:
        total_final = total_compra * 0.95
        print("Desconto de 5% aplicado!")
    else:
        total_final = total_compra

    print(f"Resumo: {itens_comprados} itens. Total a pagar: R$ {total_final:.2f}")

processar_vendas()

