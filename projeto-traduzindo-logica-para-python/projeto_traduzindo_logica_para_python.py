  n2 = float(input("Nota 2: "))



  media_aluno = (n1 + n2) / 2



  if media_aluno >= 7.0:

      print(f"{aluno_nome}: Aprovado com média {media_aluno:.2f}")

  elif media_aluno >= 5.0:

      print(f"{aluno_nome}: Recuperação (Média: {media_aluno:.2f})")

  else:

      print(f"{aluno_nome}: Reprovado")

